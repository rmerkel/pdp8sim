#ifndef	OPCODE_H
#define	OPCODE_H

#include <ostream>

/********************************************************************************************//**
 * PDP-8 Operation codes for the basic instructions
 ************************************************************************************************/
enum class OpCode {
    AND = 0,
    TAD = 1,
    ISZ = 2,
    DCA = 3,
    JMS = 4,
    JMP = 5,
    IOT = 6,
    OPR = 7
};

/********************************************************************************************//**
 * ostream put operator
 ************************************************************************************************/
std::ostream& operator<< (std::ostream& os, const OpCode& op);

#endif
